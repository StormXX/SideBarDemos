import Foundation
import UIKit

extension UIColor {
    class func uberDarkColor() -> UIColor {
        return UIColor(red: 10/255.0, green: 8/255.0, blue: 28/255.0, alpha: 1.0)
    }
    
    class func uberLightDarkColor() -> UIColor {
        return UIColor(red: 36.0/255.0, green: 33.0/255.0, blue: 53.0/255.0, alpha: 1.0)
    }
}

